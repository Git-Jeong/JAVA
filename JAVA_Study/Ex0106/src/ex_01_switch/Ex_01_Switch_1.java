package ex_01_switch; 

public class Ex_01_Switch_1 {
	public static void main(String[] args) {
		// Switch문 학습용 
		
		switch (4/2) { 
			case 2 : 
				System.out.println("2입니다. ");
				break; 
			case 3 : 
				System.out.println("2입니다. ");
				break; 
			case 4 : 
				System.out.println("2입니다. ");
				break;
			default:
				System.out.println("정답이 없습니다. ");
				break;
		}
			
	}
}
