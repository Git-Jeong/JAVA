package ex_01_var; 

public class Ex_02 {

	public static void main(String[] args) {

		
		boolean b_test_1 = true;
		System.out.println("booblean = " + b_test_1);
		
		//char : -32,768 ~ 32,767
		char c_test_1= 'A';
		System.out.println("char = " + c_test_1);
		
		String s_name_1 = "JEONGJIN MOON";
		System.out.println("String = " + s_name_1);
		
		//byte : -128 ~ 127
		byte byte_Num_1 = -120;
		System.out.println("byte = " + byte_Num_1 );
		
		int i_test_1 = 9427;
		System.out.println("int = " + i_test_1);
		
		double double_test_1 = 314;
		System.out.println("double = " + double_test_1);
		
		float f_test_1 = 1.24f;
		System.out.println("float = " + f_test_1);

		float f1 = 10.0f;
		float f2 = 7.0f;
		System.out.println("f1/f2 = " + f1/f2);

		double d1 = 10.0f;
		double d2 = 7.0f;
		System.out.println("d1/d2 = " + d1/d2);
		
		
		 
	}
}
